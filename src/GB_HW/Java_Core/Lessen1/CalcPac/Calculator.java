package GB_HW.Java_Core.Lessen1.CalcPac;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int diff(int a, int b) {
        return a - b;
    }

    public static int deriv(int a, int b) {
        return a * b;
    }

    public static int divis(int a, int b) {
        return a / b;
    }
}
